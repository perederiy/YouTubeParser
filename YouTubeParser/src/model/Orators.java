package model;

public class Orators {

	private String name;
	private String nameOrators;
	private String url;
	private String description;
	private String date;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNameOrators() {
		return nameOrators;
	}
	public void setNameOrators(String nameOrators) {
		this.nameOrators = nameOrators;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
	
}
